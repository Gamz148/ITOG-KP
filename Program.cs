/* 
Задача: Написать программу, которая из имеющегося массива строк формирует новый массив 
из строк, длина которых меньше, либо равна 3 символам. Первоначальный массив 
можно ввести с клавиатуры, либо задать на старте выполнения алгоритма. 
При решении не рекомендуется пользоваться коллекциями, лучше обойтись 
исключительно массивами.

Примеры:
[“Hello”, “2”, “world”, “:-)”] → [“2”, “:-)”]
[“1234”, “1567”, “-2”, “computer science”] → [“-2”]
[“Russia”, “Denmark”, “Kazan”] → []
*/

static void Main()
{
    Console.Write("Введите элементы массива через запятую: ");
    string input = Console.ReadLine();

    // Разбиваем строку на элементы массива
    string[] originalArray = input.Split(',');

    // Создаем список для хранения нового массива
    List<string> newArray = new List<string>();

    // Проходим по всем элементам исходного массива
    foreach (string str in originalArray)
    {
        // Если длина строки меньше или равна 3 символам, добавляем ее в новый массив
        if (str.Length <= 3)
        {
            newArray.Add(str);
        }
    }
    // Преобразуем список в массив и выводим его на экран
    string[] resultArray = newArray.ToArray();
    Console.WriteLine("Новый массив: {0}", string.Join(", ", resultArray));
}


Main();
